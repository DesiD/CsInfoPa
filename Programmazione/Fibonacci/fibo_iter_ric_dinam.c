/****************************************************************************/
/*								LIBRARIES									*/
/****************************************************************************/
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

/****************************************************************************/
/*							STRUCT DECLARATION								*/
/****************************************************************************/
int *T;

/****************************************************************************/
/*							FORWARD DECLARATIONS							*/
/****************************************************************************/
int fibo_iter(int N);
int fibo_ric(int N);
int fibo_dinam(int N);

/****************************************************************************/
/*									MAIN									*/
/****************************************************************************/
int main () {
	int N,i;
	printf("Inserisci n: ");
	scanf("%d",&N);
	T = (int *) malloc ( (N+1)*sizeof(int) );
	T[0] = 0;
	T[1] = 1;
	for(i=2;i<=N;i++)
		T[i] = -1;
	printf("FIbonacci iterativo = %d\n",fibo_iter(N));
	printf("FIbonacci ricorsivo = %d\n",fibo_ric(N));
	printf("FIbonacci dinamica  = %d\n",fibo_dinam(N));
    return 0;
}

/****************************************************************************/
/*							FIBONACCI ITERATIVO								*/
/****************************************************************************/
int fibo_iter(int N){
	int i,
		fib,
		fib_1 = 1,
		fib_2 = 0;
	for(i=2;i<=N;i++)
	{
		fib = fib_1 + fib_2;
		fib_2 = fib_1;
		fib_1 = fib;
	}
	return fib;
}

/****************************************************************************/
/*							FIBONACCI RICORSIVO								*/
/****************************************************************************/
int fibo_ric(int N){
	if(N == 0) return 0;
	if(N == 1) return 1;
	return fibo_ric(N-1) + fibo_ric(N-2);
}

/****************************************************************************/
/*							FIBONACCI DINAMICA								*/
/****************************************************************************/
int fibo_dinam(int N){
	if(T[N] == -1)
		T[N] = fibo_dinam(N-1) + fibo_dinam(N-2);
	return T[N];
}
